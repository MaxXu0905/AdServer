package com.ailk.main.geo;

public class GeoLocation {
	double lng;
	double lat;

	public GeoLocation() {
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}
}
